    private void VerifyButtonActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        int[][] grid=unloadBoard();
        runSingleThread valid=new runSingleThread();

        if(valid.validate(grid)){
            JOptionPane.showMessageDialog(null,"Valid");
        }
        else
            JOptionPane.showMessageDialog(null,"Invalid");

    }

    private void UndoButtonActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        try {
            Move lastMove=pm.undoLastMove();
            String value= String.valueOf(lastMove.getOldValue());
            cells[lastMove.getX()][lastMove.getY()].setText(value);
            cells[lastMove.getX()][lastMove.getY()].setEditable(true);
            grid[lastMove.getX()][lastMove.getY()]=lastMove.getOldValue();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"No Moves to Undo");
        }



    }

    private void SolveButtonActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        if(Game.getEmptyCellCount(unloadBoard())!=5){
            JOptionPane.showMessageDialog(null,"You Need Exactly 5 Empty Spaces!");
        }
        else {
            SudokuSolver solver = new SudokuSolver();
            try {
               int[][] result= solver.solveGame(unloadBoard());
               for(int i=0;i<5;i++){
                   cells[result[i][0]][result[i][1]].setText(String.valueOf(result[i][2]));
                   cells[result[i][0]][result[i][1]].setBackground(new Color(210,240,210));
                   cells[result[i][0]][result[i][1]].setEditable(false);
               }
            } catch (SudokuSolver.InvalidGame e) {
                throw new RuntimeException(e);
            }
        }
    }

